% Here is the constraint function as per Eq. (9) in Ferreira et al. (2016)
function [c,ceq] = mycon(r,s)
c = (2*r(1)*r(4)*s(5)^2 - r(2)^2*s(3)^2 - r(3)^2*s(2)^2 - r(4)^2*s(2)^2 -...
    r(4)^2*s(3)^2 - r(4)^2*s(5)^2 - r(1)^2*s(5)^2 + 2*r(2)*r(4)*s(3)^2 +...
    2*r(3)*r(4)*s(2)^2 + r(3)^2*s(1)*s(4) + r(2)^2*s(1)*s(6) +...
    r(1)^2*s(4)*s(6) + 2*r(4)^2*s(2)*s(3) + r(4)^2*s(1)*s(4) +...
    r(4)*s(1)*s(5)^2 - 2*r(4)^2*s(1)*s(5) + r(4)*s(3)^2*s(4) +...
    2*r(4)^2*s(2)*s(5) - 2*r(4)^2*s(3)*s(4) + 2*r(4)^2*s(3)*s(5) +...
    r(4)^2*s(1)*s(6) + r(4)*s(2)^2*s(6) - 2*r(4)^2*s(2)*s(6) +...
    r(4)^2*s(4)*s(6) + 2*r(1)*r(2)*s(3)*s(5) - 2*r(1)*r(2)*s(2)*s(6) +...
    2*r(1)*r(3)*s(2)*s(5) - 2*r(1)*r(3)*s(3)*s(4) + 2*r(2)*r(3)*s(2)*s(3) -...
    2*r(2)*r(3)*s(1)*s(5) - 2*r(1)*r(4)*s(2)*s(5) + 2*r(1)*r(4)*s(3)*s(4) -...
    2*r(2)*r(4)*s(2)*s(3) - 2*r(1)*r(4)*s(3)*s(5) + 2*r(2)*r(4)*s(1)*s(5) +...
    2*r(1)*r(4)*s(2)*s(6) - 2*r(3)*r(4)*s(2)*s(3) - 2*r(2)*r(4)*s(3)*s(5) -...
    2*r(3)*r(4)*s(1)*s(4) - 2*r(2)*r(4)*s(1)*s(6) + 2*r(3)*r(4)*s(1)*s(5) -...
    2*r(1)*r(4)*s(4)*s(6) + 2*r(2)*r(4)*s(2)*s(6) - 2*r(3)*r(4)*s(2)*s(5) +...
    2*r(3)*r(4)*s(3)*s(4) - 2*r(4)*s(2)*s(3)*s(5) -...
    r(4)*s(1)*s(4)*s(6))/(- s(6)*s(2)^2 + 2*s(2)*s(3)*s(5) -...
    s(4)*s(3)^2 - s(1)*s(5)^2 + s(1)*s(4)*s(6))^(4/3);
ceq = [];
end